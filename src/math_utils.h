/**
 * @file math_utils.h
 * @brief Simple math utilities.
 */

#ifndef MATH_UTILS_H
#define MATH_UTILS_H

/**
 * @brief Add two integers.
 * @param a First integer.
 * @param b Second integer.
 * @return Sum of a and b.
 */
int add(int a, int b);

/**
 * @brief Multiply two integers.
 * @param a First integer.
 * @param b Second integer.
 * @return Product of a and b.
 */
int multiply(int a, int b);

#endif // MATH_UTILS_H
